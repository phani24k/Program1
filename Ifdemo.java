import java.util.Scanner;
public class Ifdemo {

	public static void main(String[] args) {
		int a,b;
		Scanner sc = new Scanner(System.in);
		a= sc.nextInt();
		b= sc.nextInt();
		System.out.println("Enter the no.1="+a);
		System.out.println("Enter the no.2="+b);
		System.out.println("Sum of the two no's is"+(a+b));
		System.out.println("Sub of the two no's is"+(a-b));
		System.out.println("multiplication of the two no's is"+(a*b));
		System.out.println("division of the two no's is"+(a/b));
		System.out.println("modulo of the two no's is"+(a%b));
		
		

	}

}
